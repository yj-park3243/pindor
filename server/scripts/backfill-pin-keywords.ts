async function main() {
  const dbUrl = new URL(process.env.DATABASE_URL!);
  const client = new Client({
    host: dbUrl.hostname,
    port: dbUrl.port ? Number(dbUrl.port) : 5432,
    user: decodeURIComponent(dbUrl.username),
    password: decodeURIComponent(dbUrl.password),
    database: dbUrl.pathname.replace(/^\//, ''),
    ssl: { rejectUnauthorized: false },
  });
  await client.connect();

  // 컬럼 보장
  await client.query(
    `ALTER TABLE pins ADD COLUMN IF NOT EXISTS search_keywords TEXT[] NOT NULL DEFAULT '{}';`,
  );

  // 모든 DONG 핀 조회
  const { rows } = await client.query<{ id: string; name: string }>(
    `SELECT id, name FROM pins WHERE level='DONG'`,
  );

  let updated = 0;
  let unmapped: string[] = [];

  for (const row of rows) {
    const keywords = PIN_KEYWORDS[row.name];
    if (keywords === undefined) {
      unmapped.push(row.name);
      continue;
    }
    await client.query(
      `UPDATE pins SET search_keywords = $1::text[] WHERE id = $2`,
      [keywords, row.id],
    );
    updated++;
  }

  console.log(`업데이트: ${updated}/${rows.length}개`);
  if (unmapped.length > 0) {
    console.log(`매핑 누락 (이름 검색만 가능):`);
    for (const n of unmapped) console.log(`  - ${n}`);
  }
  await client.end();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
